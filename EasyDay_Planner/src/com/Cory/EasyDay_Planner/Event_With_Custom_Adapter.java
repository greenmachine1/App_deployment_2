package com.Cory.EasyDay_Planner;




// helper class
public class Event_With_Custom_Adapter {

	public int icon;
	public String title;
	public String extraInfo;
	
	// constructor
	public Event_With_Custom_Adapter(){
		super();
	}
	
	
	public Event_With_Custom_Adapter(int icon, String title, String extraInfo){
		
		super();
		this.icon = icon;
		this.title = title;
		this.extraInfo = extraInfo;
		
	}
	
}
